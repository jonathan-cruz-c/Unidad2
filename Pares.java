class Tabla7 {// Nombre de la clase
    public static void main(String[] args) {// Metodo main
        int a = 7;// Declaramos una variable que tiene el valor 7
        for (int i = 1; i <= 10; i++) {// Usamos el bucle for para recorrer los valores del 1 al 10
            System.out.println(a + " x " + i + " = " + (a * i));// Imprime la tabla del 7
        }
    }
}