public class NumerosPares { //En esta linea se define el nombre de la clase como 'NumerosPares' con la nomenclatura Upper Camel Case
    public static void main(String[] args) {  //Esta linea es la de la función main, que es el primer método que se ejecuta del programa
        for (int i = 2; i <= 1000; i += 2) { // Bucle for que itera un incrementamos de 2 en 2 de una variable 'i' y va imprimiendo en pantalla cada iteración
            System.out.println(i); //instrucción para mostrar en pantalla el valor de la variable 'i'
        }  // Llave de cierre del ciclo for
    }  //Llave de cierre de la función main
}  //Llave de cierre de la clase
